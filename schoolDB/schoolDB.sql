CREATE TABLE BRANCH(
    branchID CHAR(255) NOT NULL PRIMARY KEY,
    address VARCHAR(255) NOT NULL,
    city VARCHAR(255) NOT NULL
);

CREATE TABLE PARENTS(
    parentsID CHAR(255) NOT NULL PRIMARY KEY,
    father_fName VARCHAR(255) NOT NULL,
    father_lName VARCHAR(255),
    father_DOB DATE NOT NULL,
    father_occupation VARCHAR(255),
    father_address VARCHAR(255),
    father_phoneNo VARCHAR(255),
    mother_fName VARCHAR(255) NOT NULL,
    mother_lName VARCHAR(255),
    mother_DOB DATE NOT NULL,
    mother_occupation VARCHAR(255),
    mother_address VARCHAR(255),
    mother_phoneNo VARCHAR(255)
);

CREATE TABLE STUDENT(
    studentID INT NOT NULL PRIMARY KEY,
    parentsID CHAR(255) NOT NULL,
    fName VARCHAR(255) NOT NULL,
    lName VARCHAR(255),
    gender VARCHAR(10) NOT NULL,
    DOB DATE NOT NULL,
    address VARCHAR(255) NOT NULL,
    phoneNo CHAR(10),
    classYear CHAR(2) NOT NULL,
    schoolFee DECIMAL(12,2) NOT NULL,
    FOREIGN KEY (parentsID) REFERENCES PARENTS(parentsID)
);

CREATE TABLE STAFF(
    staffID CHAR(255) NOT NULL PRIMARY KEY,
    fName VARCHAR(255) NOT NULL,
    lName VARCHAR(255),
    gender VARCHAR(10) NOT NULL,
    DOB DATE NOT NULL,
    address VARCHAR(255) NOT NULL,
    phoneNo CHAR(10),
    staffType VARCHAR(255) NOT NULL,
    salary decimal (12,2) NOT NULL,
    branchID CHAR(255) NOT NULL,
    FOREIGN KEY (branchID) REFERENCES BRANCH(branchID)
);


CREATE TABLE COURSE(
    courseID CHAR(255) NOT NULL PRIMARY KEY,
    courseName VARCHAR(255) NOT NULL,
    roomID CHAR(4) NOT NULL,
    year YEAR,
    staffID CHAR(255) NOT NULL,
    FOREIGN KEY (staffID) REFERENCES STAFF(staffID)
);

CREATE TABLE EXAM(
    examID CHAR(255) NOT NULL PRIMARY KEY,
    examName VARCHAR(255) NOT NULL,
    date DATE
);

CREATE TABLE GRADE(
    gradeID CHAR(255) NOT NULL PRIMARY KEY,
    studentID INT NOT NULL,
    courseID CHAR(255) NOT NULL,
    examID CHAR(255) NOT NULL,
    grade decimal(5,2) NOT NULL,
    FOREIGN KEY (studentID) REFERENCES STUDENT(studentID),
    FOREIGN KEY (courseID) REFERENCES COURSE(courseID),
    FOREIGN KEY (examID) REFERENCES EXAM(examID)
);

CREATE TABLE ATTENDANCE(
    attendanceID CHAR(255) NOT NULL PRIMARY KEY,
    studentID INT NOT NULL,
    date DATE NOT NULL,
    status VARCHAR(10) NOT NULL,
    description VARCHAR(255),
    FOREIGN KEY (studentID) REFERENCES STUDENT(studentID)
);


